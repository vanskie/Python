def city_country(city, country):
    """Returns the city and country neatly"""
    
    result = city + ', ' + country
    
    return result

print(city_country(city = 'Sofia', country = 'Bulgaria'))
print(city_country(city = 'Havana', country = 'Cuba'))
print(city_country(city = 'New York City', country = 'USA'))

